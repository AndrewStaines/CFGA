import * as React from 'react';
import Button from '@mui/material/Button';
import Dialog from '@mui/material/Dialog';
import DialogActions from '@mui/material/DialogActions';
import DialogContent from '@mui/material/DialogContent';
import DialogContentText from '@mui/material/DialogContentText';
import DialogTitle from '@mui/material/DialogTitle';
import Slide from '@mui/material/Slide';

const Transition = React.forwardRef(function Transition(props, ref) {
  return <Slide direction="up" ref={ref} {...props} />;
});

export default function AlertDialogSlide() {
  const [open, setOpen] = React.useState(false);

  const handleClickOpen = () => {
    setOpen(true);
  };

  const handleClose = () => {
    setOpen(false);
  };

  return (
    <React.Fragment>
      <Button variant="outlined" size='large' sx={{fontSize:20}} onClick={handleClickOpen}>
        Vision
      </Button>
      <Dialog
        open={open}
        TransitionComponent={Transition}
        keepMounted
        onClose={handleClose}
        aria-describedby="alert-dialog-slide-description"
      >
        <DialogTitle>{"Vision of CFGA"}</DialogTitle>
        <DialogContent sx={{ padding: 4 }}>
          <DialogContentText id="alert-dialog-slide-description">
            The vision of a church often involves a forward-looking perspective on its desired future impact and role in the lives of its members and the community. 
          </DialogContentText>
        </DialogContent>
      </Dialog>
    </React.Fragment>
  );
}